package entity;

/**
 * Класс-сущность вектор в двумерном пространстве
 */
public class Vector {
    /** Координата X (абсцисса) вектора */
    private double x;

    /** Координата Y (ордината) вектора */
    private double y;

    /**
     * Создает новый вектор с указанными координатами.
     *
     * @param x координата X (абсцисса)
     * @param y координата Y (ордината)
     *
     */
    public Vector(double x, double y) {
        this.x = x;
        this.y = y;
    }

    /**
     * Возвращает координату X (абсциссу) вектора.
     *
     * @return значение координаты X
     *
     */
    public double getX() {
        return x;
    }

    /**
     * Возвращает координату Y (ординату) вектора.
     *
     * @return значение координаты Y
     *
     */
    public double getY() {
        return y;
    }

    /**
     * Устанавливает новое значение координаты Y (ординаты) вектора.
     *
     * @param y новое значение координаты Y
     *
     */
    public void setY(double y) {
        this.y = y;
    }

    /**
     * Устанавливает новое значение координаты X (абсциссы) вектора.
     *
     * @param x новое значение координаты X
     *
     */
    public void setX(double x) {
        this.x = x;
    }

    /**
     * Возвращает строковое представление вектора.
     * Формат вывода: "Vector(x, y)", где x и y - координаты вектора.
     * Числа выводятся с двумя знаками после запятой для читаемости.
     *
     * @return строковое представление вектора
     */
    @Override
    public String toString() {
        return String.format("Vector(%.2f, %.2f)", x, y);
    }
}
