package interfaces;

/**
 * Интерфейс для выполнения арифметических операций над объектами типа T
 * @param <T> тип объектов, над которыми выполняются операции
 */
public interface ArithmeticOperations<T> {

    /**
     * Сложение двух объектов
     * @param a первый операнд
     * @param b второй операнд
     * @return результат сложения a + b
     */
    T add(T a, T b);

    /**
     * Вычитание двух объектов
     * @param a первый операнд
     * @param b второй операнд
     * @return результат вычитания a - b
     */
    T subtract(T a, T b);

    /**
     * Умножение двух объектов
     * @param a первый операнд
     * @param b второй операнд
     * @return результат умножения a * b
     */
    T multiply(T a, T b);

    /**
     * Деление двух объектов
     * @param a первый операнд (делимое)
     * @param b второй операнд (делитель)
     * @return результат деления a / b
     * @throws ArithmeticException если b равно нулю или операция невозможна
     */
    T divide(T a, T b);
}